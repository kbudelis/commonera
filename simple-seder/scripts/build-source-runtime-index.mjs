    runtimeSpineAvailable: ["shir-geulah", "velveteen-rabbi"].includes(definition.sourceId),
    acquisition,
    passages: (passagesBySource.get(definition.sourceId) ?? []).map((passage) => enrichPassage(passage, definition, acquisition)),
