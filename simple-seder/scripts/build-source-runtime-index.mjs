async function readJson(filename, fallback) {
  try {
    return JSON.parse(await readFile(filename, "utf8"));
  } catch (error) {
    if (error.code === "ENOENT") return fallback;
    throw error;
  }
}

const prior = await readJson(indexPath, { sources: [] });
const passages = await readJson(passagePath, []);
const passagesBySource = Map.groupBy(passages, (passage) => passage.sourceId);

function normalizePassageText(value) {
  return value.normalize("NFC").replace(/\s+/g, " ").trim();
}

function enrichPassage(passage, definition, acquisition) {
  const exactText = normalizePassageText(passage.exactText);
  const provenanceHash = createHash("sha256").update(exactText, "utf8").digest("hex");
  return {
    id: passage.id,
    sourceId: definition.sourceId,
    family: definition.family,
    exactText,
    locator: {
      edition: passage.edition ?? definition.title,
      sourceLocation: passage.sourceLocation,
      localExtract: acquisition.extractFile,
      sourceSha256: acquisition.sha256,
      extractSha256: acquisition.extractSha256,
    },
    sectionIds: passage.sectionIds,
    themes: passage.themes,
    audience: passage.audience,
    tones: passage.tones,
    lengthTiers: passage.lengthTiers,
    seam: passage.seam,
    treatment: passage.treatment ?? "verbatim-normalized",
    wordCount: exactText.split(" ").filter(Boolean).length,
    approvalStatus: passage.approvalStatus ?? "candidate-review",
    rights: {
      runtimeEligible: passage.runtimeEligible ?? true,
      license: definition.rights.license,
      materialKind: passage.materialKind ?? "creator-authored",
      sourcePresentedAttributionRequired: Boolean(passage.sourcePresented),
      conditions: passage.conditions ?? definition.rights.conditions,
    },
    provenanceHash,
    attribution: {
      creator: passage.attributionCreator ?? definition.creator,
      containingSource: definition.title,
      sourcePresented: passage.sourcePresented ?? null,
      readerCredit: passage.readerCredit ?? `From ${definition.title} by ${definition.creator}.`,
    },
  };
}

